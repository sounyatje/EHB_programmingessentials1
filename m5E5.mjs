// E5: Ingebouwde functies
// JavaScript heeft verschillende ingebouwde functies die wij als developer kunnen gebruiken om onze eigen applicaties te verbeteren. 
// Ze zijn er verschillende Math-functies. Je kan gebruik maken van Math.min() om het kleinste getal te vinden dat je mee geeft als parameter.

// Probeer dit out. Probeer ook even na te denken wat een functienaam zou kunnen zijn om het grootste getal te vinden.

// Je kan een overzicht van beschikbare functies terugvinden via de documentatieLinks to an external site..

Math.min(2,4,6,8,10)

Math.max(2,4,6,8,10)

console.log(Math.min(2,4,6,8,10))  // affiche 2
console.log(Math.max(2,4,6,8,10))  // affiche 10
